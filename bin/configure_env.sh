#!/bin/bash

export POSTGRES_USER=${POSTGRES_USER:-user}
export POSTGRES_PASSWORD=${POSTGRES_PASSWORD:-password}
export POSTGRES_HOST=${POSTGRES_HOST:-localhost}
export POSTGRES_PORT=${POSTGRES_PORT:-5432}
export POSTGRES_DB=${POSTGRES_DB:-quiz_db}
export POSTGRES_SCHEMA=${POSTGRES_SCHEMA:-public}

export DATABASE_URL=${DATABASE_URL:-"postgresql://${POSTGRES_USER}:${POSTGRES_PASSWORD}@${POSTGRES_HOST}:${POSTGRES_PORT}/${POSTGRES_DB}?schema=${POSTGRES_SCHEMA}"}

echo "${DATABASE_URL}"

export NESTJS_PORT=3001
export NEXTJS_PORT=3000
